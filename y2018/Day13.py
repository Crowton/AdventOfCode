while True:
    for (x, y) in sorted(carts.keys(), key=lambda t: (t[1], t[0])):
        if (x, y) in carts and not carts[(x, y)][3]:
            c = carts[(x, y)]
            nX = x + c[0]
            nY = y + c[1]
            dX = c[0]
            dY = c[1]
            direc = c[2]

            if (nX, nY) in carts:
                del carts[(nX, nY)]

            else:
                space = tracks[nY][nX]
                if space == "+":
                    if c[2] == "left":
                        direc = "straight"
                        dX, dY = turn[(dX, dY)]
                    elif c[2] == "straight":
                        direc = "right"
                    elif c[2] == "right":
                        direc = "left"
                        dX, dY = turn[(dX, dY)]
                        dX, dY = turn[(dX, dY)]
                        dX, dY = turn[(dX, dY)]
                elif space == "/":
                    if dX != 0:
                        dX, dY = turn[(dX, dY)]
                    else:
                        dX, dY = turn[(dX, dY)]
                        dX, dY = turn[(dX, dY)]
                        dX, dY = turn[(dX, dY)]
                elif space == "\\":
                    if dX != 0:
                        dX, dY = turn[(dX, dY)]
                        dX, dY = turn[(dX, dY)]
                        dX, dY = turn[(dX, dY)]
                    else:
                        dX, dY = turn[(dX, dY)]

                carts[(nX, nY)] = (dX, dY, direc, True)

            del carts[(x, y)]

    if len(carts) == 1:
        for c in carts:
            print(str(c[0]) + "," + str(c[1]))
            # 133,146
        break

    for c in carts:
        x, y, d, r = carts[c]
        carts[c] = (x, y, d, False)
